					Se consideran productos académicos del programa de posgrado, los siguientes:
				</p>
				<p class="ml-3">
					• Artículos de investigación indizados en el Journal Citation Repodts (JCR) o reconocidos por el Consejo Nacional de Ciencia y Tecnología (CONACyT).
				</p>
				<p class="ml-3">
					• Artículos de investigación arbitrados.
				</p>
				<p class="ml-3">
					• Libros y capítulos de libros científicos.
				</p>
				<p class="ml-3">
					• Memorias en extenso en congresos.
				</p>
				<p class="ml-3">
					• Patentes registradas y/o en proceso de explotación.
				</p>
				<p class="ml-3">
					• Desarrollos tecnológicos, prototipos experimentales registrados.
				</p>
				<p class="ml-3">
					• Modelos de utilidad.
				</p>
				<p>
					Los derechos de propiedad de la investigación y de los resultados obtenidos por los investigadores que reciban apoyo interno serán objeto de regulación específica del TecNM, en tanto que los premios y reconocimientos resultantes cuando así se establezca, serán patrimonio del profesor o investigador.
				</p>
				<p>
					Por otro lado, los resultados de las investigaciones apoyadas por empresas, organismos, asociaciones e instituciones de los sectores de la sociedad (productor de bienes y servicios, social y público) serán materia de regulación de acuerdo con los convenios específicos o bases de concertación firmados al respecto.
				</p>
				<p>
					Las tesis desarrolladas en los diversos programas de posgrado deberán contar con una de cesión de derechos.
				</p>
			</div>
			
